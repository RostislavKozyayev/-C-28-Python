# Дан список А размера N. Вывести вначале его элементы с нечетными номерами в порядке возрастания номеров, затем элементы с четными номерами в порядке убывания номеров: А1, А3, А5, ..., А6, А4, А2. Условный оператор не использовать.

# Ввод данных
n = int(input("Введите размер списка: "))
A = [int(input(f"Введите элемент A[{i + 1}]: ")) for i in range(n)]

# Элементы с нечетными номерами (индексы 0, 2, 4, ... => 1, 3, 5, ...)
odd_indexed = [A[i] for i in range(n) if i % 2 == 0]

# Элементы с четными номерами (индексы 1, 3, 5, ... => 2, 4, 6, ...)
even_indexed = [A[i] for i in range(n) if i % 2 != 0][::-1]

# Результат
result = odd_indexed + even_indexed
print("Результат:", result)