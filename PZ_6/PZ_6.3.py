# Дан список размера N. Осуществить циклический сдвиг элементов списка вправо на одну позицию (при этом А, перейдет в А2. А2 в А3....., Аn - в А1).

# Ввод данных
n = int(input("Введите размер списка: "))
A = [int(input(f"Введите элемент A[{i + 1}]: ")) for i in range(n)]

# Проверка на случай пустого списка
if n > 0:
    last_element = A[-1]  # Сохраняем последний элемент
    for i in range(n - 1, 0, -1):
        A[i] = A[i - 1]  # Сдвигаем элементы вправо
    A[0] = last_element  # Ставим последний элемент на первое место

# Вывод результата
print("Результат после сдвига:", A)