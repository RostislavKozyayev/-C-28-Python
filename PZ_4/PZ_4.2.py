# Дано целое число N (>0). Найти наименьшее целое положительное число K, квадрат
# которого превосходит N: K2 > N. Функцию извлечения квадратного корня не
# использовать.

# Вводим целое число N
while True:
    try:
        N = int(input("Введите целое число N (>0): "))
        if N <= 0:
            print("Ошибка: N должно быть больше 0.")
            continue
        print("Удачно")
        break
    except ValueError:
        print("Что-то пошло не так. Повторите попытку.")

# Начинаем с K = 1
K = 1

# Находим K, такой что K^2 > N
while K * K <= N:
    K += 1

print(f"Наименьшее целое положительное число K, квадрат которого превосходит N: {K}")